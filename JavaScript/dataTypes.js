// Data Types (primitives)
//Number
let age = 25;
console.log("The type of age is: ", typeof age);

//String
let address = "#1234, nyc";
console.log("The type of name is: ", typeof address);

//Boolean
let isAdmin = false;
isAdmin = true;
console.log("The value of isAdmin is: ", typeof isAdmin);

//Null (Object) - intentional absence of data
let car = null;
console.log("The value of car is: ", typeof car);
console.log("The type of car is: ", typeof null);

//Undefined - unintentional absence of data
let city;
console.log("The type of City is: ", typeof city);