// Looping Statements - Execute repetitive tasks / automate tasks


// Loops: for loop
for (let idx = 1; idx <= 5; idx++) {
  console.log("For count", idx);
}


// Loops: while loop
let count = 1;

while (count < 5) {
  console.log("While count:", count)
  count++;
}


// Loops: do..while loop
let counter = 1;

do {
  console.log("Do..while count:", counter)
} while (counter < 1);